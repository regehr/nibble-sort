#define SORT_BYTE(c) c = sortedByte[c]

#define SORT_8_BYTES(p)                                                        \
  SORT_BYTE((p)[0]);                                                           \
  SORT_BYTE((p)[1]);                                                           \
  SORT_BYTE((p)[2]);                                                           \
  SORT_BYTE((p)[3]);                                                           \
  SORT_BYTE((p)[4]);                                                           \
  SORT_BYTE((p)[5]);                                                           \
  SORT_BYTE((p)[6]);                                                           \
  SORT_BYTE((p)[7]);

void nibble_sort_frank(unsigned long *buf) {
  unsigned char *data = (unsigned char *)buf;
  unsigned long buffer[1024];

  for (int j = 0; j < 8; ++j) {
    for (int i = 0; i < 1024; i++) {
      SORT_8_BYTES((data + 8 * i));
    }

    for (int i = 0; i < 1024; ++i) {
      buffer[i] = buf[i] & 0xf000000000000000;
      buf[i] <<= 4;
    }

    for (int i = 0; i < 1024; i++) {
      SORT_8_BYTES((data + 8 * i));
    }

    for (int i = 0; i < 1024; ++i) {
      buf[i] >>= 4;
      buf[i] |= buffer[i];
    }
  }
}
